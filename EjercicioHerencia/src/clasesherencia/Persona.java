package clasesherencia;

public class Persona {
    private String nombre;
    private int edad;

    public Persona() {

    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    public void seEdad(int edad) {
        this.edad = edad;
    }

    public int getEdad() {
        return edad;
    }
}
